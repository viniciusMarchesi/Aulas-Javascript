/** Aula sobre Variaveis
 * existem duas maneiras para criar variaveis em javascript, Let é uma forma, Var é outra,
 * são  praticamente a mesma coisa.
 * Não podemos criar variaveis com palavras reservadas.
 * variaveis precisam ter nomes significativos.
 * não pode começar o nome de uma variavel com numero.
 * não pode conter espaços ou traços.
 * utilizamos camelCase quando queremos criar uma variavel composta exemplo:
 * nomeCliente  a primeira palavras se inicia com letra minuscula e a segunda em maiuscula.
 * Case-sensitive
 * Não podemos redeclarar variaveis com let
 * não utilize var, sempre utilize let.
 */

